-- 員工列表
CREATE TABLE EMPLOYEE
(
    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    CODE VARCHAR2(100) NOT NULL,
    NAME VARCHAR2(20) NOT NULL,
    COMPANY_ID NUMBER(20) NOT NULL,
    AGE VARCHAR(255) NOT NULL,
    GENDER VARCHAR(255) NOT NULL,
    CREATE_TIME DATE DEFAULT SYSDATE NOT NULL,
    UPDATE_TIME DATE DEFAULT SYSDATE NOT NULL  
);

ALTER TABLE EMPLOYEE ADD CONSTRAINT PK_EMPLOYEE_ID PRIMARY KEY (ID);

COMMENT ON TABLE EMPLOYEE IS '員工列表';
COMMENT ON COLUMN EMPLOYEE.ID IS '流水號';
COMMENT ON COLUMN EMPLOYEE.CODE IS '員工代號';
COMMENT ON COLUMN EMPLOYEE.NAME IS '員工名稱';
COMMENT ON COLUMN EMPLOYEE.COMPANY_ID IS '所屬的公司ID, Ref: company.id';
COMMENT ON COLUMN EMPLOYEE.AGE IS '員工年齡';
COMMENT ON COLUMN EMPLOYEE.GENDER IS '員工性別';
COMMENT ON COLUMN EMPLOYEE.CREATE_TIME IS '創建時間';
COMMENT ON COLUMN EMPLOYEE.UPDATE_TIME IS '更新時間';
