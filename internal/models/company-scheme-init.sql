-- 公司列表
CREATE TABLE COMPANY
(
    ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    CODE VARCHAR2(100) NOT NULL,
    NAME VARCHAR2(100) NOT NULL,
    IS_ACTIVE NUMBER(1) DEFAULT 1 NOT NULL,
    CREATE_TIME DATE DEFAULT SYSDATE NOT NULL,
    UPDATE_TIME DATE DEFAULT SYSDATE NOT NULL  
);

ALTER TABLE COMPANY ADD CONSTRAINT PK_COMPANY_ID PRIMARY KEY (ID);

COMMENT ON TABLE COMPANY IS '公司列表';
COMMENT ON COLUMN COMPANY.ID IS '流水號';
COMMENT ON COLUMN COMPANY.CODE IS '公司代號';
COMMENT ON COLUMN COMPANY.NAME IS '公司名稱';
COMMENT ON COLUMN COMPANY.IS_ACTIVE IS '是否營業';
COMMENT ON COLUMN COMPANY.CREATE_TIME IS '創建時間';
COMMENT ON COLUMN COMPANY.UPDATE_TIME IS '更新時間';
